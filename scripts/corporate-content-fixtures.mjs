function makeDocumentText({ title, type, intent, process, instruction, admin, faq }) {
  return `= ${title} =

'''Тип документа:''' ${type}

== Назначение ==
Документ ${intent}

== Область применения ==
Материал предназначен для сотрудников, руководителей, владельцев процесса и администраторов корпоративных систем. Он используется как тестовый корпоративный контент для проверки AI-поиска, чата, источников и ACL.

== Процесс ==
${process}

== Инструкция пользователя ==
${instruction}

== Инструкция администратора ==
${admin}

== Контроль и аудит ==
Ответственный владелец проверяет актуальность документа не реже одного раза в квартал. Критичные изменения фиксируются в истории страницы и доводятся до затронутых групп.

== FAQ ==
${faq}

[[Категория:Corporate AI Seed]]
[[Категория:${type}]]
`;
}

function makeSectionIndex(namespace, department, section) {
  const links = section.docs
    .map((doc) => `* [[${namespace}:${section.name}/${doc.title}|${doc.title}]] - ${doc.type}`)
    .join('\n');

  return {
    title: `${namespace}:${section.name}`,
    text: `= ${section.name} =

'''Департамент:''' ${department}

'''Владелец:''' ${section.owner}

== Документы отдела ==
${links}

== Использование в тестах ==
Эта ветка содержит разные типы корпоративных документов для проверки релевантности поиска, ссылок на источники и разграничения доступа.

[[Категория:Corporate AI Seed]]
`,
  };
}

function makeDepartmentIndex(department) {
  const links = department.sections
    .map((section) => `* [[${department.namespace}:${section.name}|${section.name}]] - ${section.owner}`)
    .join('\n');

  return {
    title: `${department.namespace}:Главная`,
    text: `= ${department.department} =

'''Namespace:''' ${department.namespace}

== Отделы ==
${links}

== Назначение раздела ==
Раздел моделирует реальный корпоративный контент департамента ${department.shortName}. Он нужен для проверки поиска, чата, ACL и ссылок на источники в WikiAI.

[[Категория:Corporate AI Seed]]
`,
  };
}

export function getNamespaceAcl() {
  return {
    '0': ['*'],
    ...Object.fromEntries(
      CORPORATE_NAMESPACES.map((namespace) => [String(namespace.id), namespace.allowedGroups])
    ),
  };
}

export function buildCorporatePages() {
  const pages = [];

  pages.push({
    title: 'CorpCommon:Главная',
    text: `= Корпоративные документы =

Раздел содержит общие приказы и ссылки на департаментские базы знаний.

== Общие приказы ==
${COMMON_ORDERS.map((doc) => `* [[${doc.title}|${doc.title.replace('CorpCommon:Приказы/', '')}]]`).join('\n')}

== Департаменты ==
${CORPORATE_DEPARTMENTS.map((department) => `* [[${department.namespace}:Главная|${department.department}]]`).join('\n')}

[[Категория:Corporate AI Seed]]
`,
  });

  for (const order of COMMON_ORDERS) {
    pages.push({
      title: order.title,
      text: makeDocumentText(order),
    });
  }

  for (const department of CORPORATE_DEPARTMENTS) {
    pages.push(makeDepartmentIndex(department));
    for (const section of department.sections) {
      pages.push(makeSectionIndex(department.namespace, department.department, section));
      for (const doc of section.docs) {
        pages.push({
          title: `${department.namespace}:${section.name}/${doc.title}`,
          text: makeDocumentText(doc),
        });
      }
    }
  }

  return pages;
}
